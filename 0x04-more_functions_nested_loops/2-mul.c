#include <unistd.h>
/**
 * mul - multiplies two numbers
 *
 * @a: the first number to be passed
 *
 * @b: the second num to be multiplied
 *
 * Description: mul will multiply two numbers a and b
 *
 * Return: the value of the answer
 */
int mul(int a, int b)
{
	int answer;

	answer = a * b;

	return (answer);
}
